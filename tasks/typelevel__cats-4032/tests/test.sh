#!/bin/bash

cd /app/src

# Copy HEAD test files from /tests (overwrites BASE state)
mkdir -p "tests/src/test/scala-2.12/cats/tests"
cp "/tests/src/test/scala-2.12/cats/tests/ScalaVersionSpecific.scala" "tests/src/test/scala-2.12/cats/tests/ScalaVersionSpecific.scala"
mkdir -p "tests/src/test/scala-2.13+/cats/tests"
cp "/tests/src/test/scala-2.13+/cats/tests/ScalaVersionSpecific.scala" "tests/src/test/scala-2.13+/cats/tests/ScalaVersionSpecific.scala"
mkdir -p "tests/src/test/scala/cats/tests"
cp "/tests/src/test/scala/cats/tests/AlgebraInvariantSuite.scala" "tests/src/test/scala/cats/tests/AlgebraInvariantSuite.scala"

# Run only the specific test class for this PR
sbt "testsJVM / testOnly cats.tests.AlgebraInvariantSuite"
test_status=$?

if [ $test_status -eq 0 ]; then
  echo 1 > /logs/verifier/reward.txt
else
  echo 0 > /logs/verifier/reward.txt
fi
exit "$test_status"
